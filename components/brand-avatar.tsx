function detectBrandPath(from: string): string | null {
  const lower = from.toLowerCase();
  for (const [keyword, key] of BRAND_KEYWORDS) {
    if (lower.includes(keyword)) return BRAND_PATHS[key] ?? null;
  }
  return null;
}

